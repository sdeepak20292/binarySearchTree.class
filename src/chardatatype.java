public class chardatatype {
    public static void main (String[] args) {
        // char is a datatype which can store a single character let it be int or string literal.

        char mychar = 'D';
        char myunicode = '\u0044'; // this is unicode for D
        System.out.println(mychar);
        System.out.println(myunicode);

        // boolean

        boolean mybool = true;
        boolean mynotbool = false;
        System.out.println(mybool);
        System.out.println(mynotbool);



    }
}
